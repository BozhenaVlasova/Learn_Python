def get_length_of_missing_array(array_of_arrays):
    for i in array_of_arrays:
        if i == [] or i ==  None:
            return 0
    arr = sorted([len(i) for i in array_of_arrays if i is not None])
    if arr == []:
        return 0
    else:
        norm_arr = [i for i in range(min(arr), max(arr)+1)]
        res = [i for i in norm_arr if i not in arr]
        return(res[0])


